"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Marcelo Martins')
print(1, 'Marcelo "Martins"')

# Aspas duplas
print("Marcelo Martins")
print(2, "Marcelo 'Martins'")


# Escape
print("Marcelo \"Martins\"")

# r
print(r"Marcelo \"Martins\"")
